CREATE TABLE Alumno(
NIF VARCHAR(10) PRIMARY KEY NOT NULL,
nombre VARCHAR(20),
trabaja BOOLEAN,
estudios SET('ESO','BACHILLERATO','FP MEDIO','FP SUPERIOR', 'GRADO UNIVERSITARIO'),
ingresos DECIMAL(18,2),
fecha_de_nacimiento DATE
);
